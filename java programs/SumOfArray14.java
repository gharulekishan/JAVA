package java_programms;

public class SumOfArray14 {

	public static void main(String[] args) {
		
		
	int a []= {10,12,51,20,40,65};
	
    int sum= 0;
    
    for (int value : a)
    {
    	sum=sum+ value;
    
    }
   
   
    System.out.println("sum of the array elements :  " + sum);
		
     
	}

}
